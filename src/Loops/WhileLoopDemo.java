package Loops;

public class WhileLoopDemo {
    public static void main(String[] args) {
        //going gym  5 gym(0-4 or 1-5 or 101 - 105 ... )
        int i = 1;
        while(i<=5){
            System.out.println("Going to gym" + i);
            i = i+ 1;
        }
        System.out.println("GYM OUT" + i);
    }
}
